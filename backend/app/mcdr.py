# 状态常量
STATUS_INSTALLING = "installing"
STATUS_ERROR = "error"
STATUS_NEW_SETUP = "new_setup"
STATUS_STARTING = "starting"
STATUS_RUNNING = "running"
STATUS_STOPPED = "stopped"

# 服务端加载完成标志:vanilla 输出 'Done (12.345s)! For help, ...'
_DONE_RE = re.compile(r"Done \([\d.,]+s\)!")

_INSTALLING_MARKER = ".installing"
_FAILED_MARKER = ".install_failed"

# 控制台:每个实例在内存里保留的最近日志行数,以及订阅队列的上限。
_CONSOLE_BUFFER_LINES = 500
_SUBSCRIBER_QUEUE_MAXSIZE = 1000


def sanitize_dir_name(name: str) -> str:
    """把服务器名净化成安全的目录名。"""
    cleaned = re.sub(r"[^A-Za-z0-9_.-]+", "_", name.strip())
    cleaned = cleaned.strip("._") or "server"
    return cleaned[:80]


def _eula_text() -> str:
    return (
        "# Generated by mc-panel. By setting eula=true you agree to the "
        "Minecraft EULA (https://aka.ms/MinecraftEULA).\n"
        "eula=true\n"
    )


def _server_properties_text(port: int) -> str:
    return "\n".join(
        [
            "#Minecraft server properties (generated by mc-panel)",
            f"server-port={port}",
            "online-mode=true",
            "max-players=20",
            "view-distance=10",
            "motd=A Minecraft Server",
            "enable-rcon=false",
            "",
        ]
    )


# 服务器类型 -> MCDR 输出解析 handler(均为 MCDR 内置)
HANDLERS = {
    "vanilla": "vanilla_handler",
    "fabric": "vanilla_handler",
    "forge": "forge_handler",
    "velocity": "velocity_handler",
}


def _resolve_launch(inst: "Path", server_type: str) -> tuple[str, str]:
    """根据已安装文件决定启动目标:('-jar', jar) 或 ('@', argsfile)。"""
    sd = inst / "server"
    if server_type == "forge":
        # 现代 Forge(1.17+):@libraries/.../win_args.txt
        args = sorted(sd.glob("libraries/net/minecraftforge/forge/*/win_args.txt"))
        if args:
            return ("@", str(args[0].relative_to(sd)).replace("\\", "/"))
        # 旧版 Forge:forge-*.jar(排除 installer)
        jars = [p for p in sd.glob("forge-*.jar") if "installer" not in p.name]
        if jars:
            return ("-jar", jars[0].name)
    return ("-jar", "server.jar")


def build_start_command(
    java_command: str,
    min_memory: str,
    max_memory: str,
    extra_jvm_args: str = "",
    server_type: str = "vanilla",
    launch: tuple[str, str] = ("-jar", "server.jar"),
) -> list[str]:
    """组装服务端启动命令:java + 内存 + UTF-8 + 额外参数 + 启动目标。"""
    try:
        extra = shlex.split(extra_jvm_args) if extra_jvm_args else []
    except ValueError:
        extra = extra_jvm_args.split()
    cmd = [
        java_command,
        f"-Xms{min_memory}",
        f"-Xmx{max_memory}",
        # 强制服务端以 UTF-8 输出,避免 Windows 下控制台用系统码页(GBK)导致乱码
        "-Dfile.encoding=UTF-8",
        *extra,
    ]
    mode, value = launch
    if mode == "@":
        cmd.append(f"@{value}")
    else:
        cmd += ["-jar", value]
    if server_type != "velocity":  # 代理端无 GUI 概念
        cmd.append("nogui")
    return cmd


def _mcdr_config(
    java_command: str,
    min_memory: str,
    max_memory: str,
    extra_jvm_args: str = "",
    server_type: str = "vanilla",
    launch: tuple[str, str] = ("-jar", "server.jar"),
) -> dict:
    """构造 MCDR config.yml 内容(handler 与启动命令按服务器类型)。"""
    return {
        "language": "zh_cn",
        "working_directory": "server",
        "start_command": build_start_command(
            java_command, min_memory, max_memory, extra_jvm_args, server_type, launch
        ),
        "handler": HANDLERS.get(server_type, "vanilla_handler"),
        # 发送给服务端用 UTF-8;读取服务端输出先按 UTF-8,失败回退 GBK
        # (Windows 下 Java 启动器/旧服务端常用系统码页),避免解码报错。
        "encoding": "utf8",
        "decoding": ["utf8", "gbk"],
        "plugin_directories": ["plugins"],
        "check_update": False,
        "advanced_console": False,
        "telemetry": False,
    }


